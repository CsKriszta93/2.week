using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TwoNumbers
{
    class TwoNumbers
    {
        static void Main(string[] args)
        {
            var a = 22;
            var b = 13;

            Console.WriteLine(a + b);
            Console.WriteLine(a - b);
            Console.WriteLine(a * b);
            Console.WriteLine(22.0 / 13.0);
            Console.WriteLine(22 / 13);
            Console.WriteLine(22 % 13);

            Console.ReadLine();
        }
    }
}
